# Desafio 009
# Faça um programa que leia um número inteiro qualquer e mostre na tela a sua tabuada
print('####TABUEITOR PRÓ PLUS 3.0#####')

a = int(input('Digite um número: '))
print('--'*5)
print('{}X1 = {} \n{}X2 = {} \n{}X3 = {} \n{}X4 = {} \n{}X5 = {}'
      .format(a, a, a, (a*2), a, (a*3), a, (a*4), a, (a*5)))
print('{}X6 = {} \n{}X7 = {} \n{}X8 = {} \n{}X9 = {} \n{}X10 = {}'
      .format(a, (a*6), a, (a*7), a, (a*8), a, (a*9), a, (a*10)))
print('--'*5)
